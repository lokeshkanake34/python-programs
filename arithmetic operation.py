# Define two variables to work with
a = 15
b = 4

# Basic Arithmetic
addition = a + b        # 15 + 4
subtraction = a - b     # 15 - 4
multiplication = a * b  # 15 * 4
division = a / b        # 15 / 4 (always returns a float)

# Specialized Operations
modulus = a % b         # Remainder of 15 / 4
exponent = a ** b       # 15 to the power of 4
floor_division = a // b # 15 / 4 (rounds down to the nearest whole number)

# Displaying the results
print(f"Addition (15 + 4): {addition}")
print(f"Subtraction (15 - 4): {subtraction}")
print(f"Multiplication (15 * 4): {multiplication}")
print(f"Division (15 / 4): {division}")
print(f"Modulus (Remainder): {modulus}")
print(f"Exponent (15^4): {exponent}")
print(f"Floor Division: {floor_division}")
